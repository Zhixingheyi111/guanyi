// 快速查找
export function getGlossaryEntry(id) {
  return glossary.find(g => g.id === id) || null;
}

// 按分类分组
export function getGlossaryByCategory() {
  const map = {};
  CATEGORIES.forEach(cat => { map[cat.id] = []; });
  glossary.forEach(entry => {
    if (map[entry.category]) map[entry.category].push(entry);
  });
  return map;
}
